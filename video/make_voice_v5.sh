#!/bin/bash
# संस्कृता Video #3 "First Program in 5 Minutes" — narration (neural female voice)
# Run:  bash make_voice_v5.sh
set -e
cd "$(dirname "$0")"
mkdir -p v3
python3 -m pip install --user --quiet edge-tts

VOICE="hi-IN-SwaraNeural"
speak() {
    python3 -m edge_tts --voice "$VOICE" --rate="-5%" --text "$2" --write-media "v3/line$1.mp3"
    echo "✓ v3/line$1.mp3"
}

speak 1 "संस्कृता में अपना पहला प्रोग्राम — सिर्फ़ पाँच मिनट में। चलिए, शुरू करते हैं!"
speak 2 "चाहिए बस दो चीज़ें — पाइथन थ्री, जो ज़्यादातर कंप्यूटर में पहले से होता है, और हमारा गिटहब रेपो।"
speak 3 "टर्मिनल खोलिए। पहले रेपो को क्लोन कीजिए, फिर लिखिए — बैश इंस्टॉल डॉट एस एच। बस! संस्कृता तैयार है।"
speak 4 "अब एक फ़ाइल बनाइए — नमस्ते डॉट सं। और उसमें लिखिए — वद, नमस्ते जगत्। अंत में दण्ड लगाना मत भूलिए!"
speak 5 "अब चलाइए — संस्कृता नमस्ते डॉट सं। और देखिए — आपका पहला संस्कृत प्रोग्राम बोल उठा!"
speak 6 "देवनागरी टाइप करना मुश्किल लगे? कोई बात नहीं। संस्कृता प्लेग्राउंड चलाइए — रोमन में टाइप कीजिए, अपने आप देवनागरी बन जाएगी!"
speak 7 "और आगे? रेपो में पूरा ट्यूटोरियल है — दस प्रोग्राम, एक घंटे में। साथ में ए आई स्पेक भी — किसी भी ए आई को अपना संस्कृता गुरु बनाइए!"
speak 8 "तो आज ही अपना पहला प्रोग्राम लिखिए — और गिटहब पर पोस्ट करके हमें ज़रूर बताइए। जयतु संस्कृतम्!"

echo ""
echo "सिद्धम् ✓ Tutorial narration ready (video/v3/). Tell Claude!"
