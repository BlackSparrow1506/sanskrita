#!/bin/bash
# संस्कृता Video #2 "The Story" v2 — positive framing (neural female voice)
# Run:  bash make_voice_v4.sh
set -e
cd "$(dirname "$0")"
mkdir -p v2
python3 -m pip install --user --quiet edge-tts

VOICE="hi-IN-SwaraNeural"
speak() {
    python3 -m edge_tts --voice "$VOICE" --rate="-6%" --text "$2" --write-media "v2/line$1.mp3"
    echo "✓ v2/line$1.mp3"
}

speak 1 "सन् उन्नीस सौ पचासी। नासा के वैज्ञानिक रिक ब्रिग्स ने लिखा — संस्कृत, कंप्यूटर के लिए दुनिया की सबसे उपयुक्त भाषा है।"
speak 2 "और इसकी नींव? आज से लगभग ढाई हज़ार साल पहले पाणिनि ने चार हज़ार सूत्रों में पूरी भाषा का व्याकरण लिख दिया — बिल्कुल किसी प्रोग्राम की तरह।"
speak 3 "संस्कृत की शक्ति देखिए — हर शब्द वैसा ही बोला जाता है, जैसा लिखा जाता है। हर वाक्य का अर्थ — बिल्कुल स्पष्ट। मानो यह भाषा कंप्यूटर के लिए ही बनी हो।"
speak 4 "तो हमने सोचा — जो भाषा इतनी परिपूर्ण है, क्या वह स्वयं एक प्रोग्रामिंग भाषा नहीं बन सकती? क्यों नहीं!"
speak 5 "इसीलिए हमने बनाई — संस्कृता! असली प्रोग्रामिंग भाषा, पूरी तरह संस्कृत में। वद। यदि। यावत्। विधि। और पाणिनि के कारक भी।"
speak 6 "यह कोई अनुवाद नहीं — इसका अपना इंजन है। दशमलव हमेशा शुद्ध। संधि और छन्द — भाषा के भीतर ही बने हुए।"
speak 7 "हमारा सपना — संस्कृता दुनिया की श्रेष्ठ भाषाओं के बीच गर्व से खड़ी हो। इसीलिए यह ओपन सोर्स है — सबके लिए, हमेशा मुफ़्त।"
speak 8 "और यह तो केवल शुरुआत है। जयतु संस्कृतम्!"

echo ""
echo "सिद्धम् ✓ Positive story narration ready. Tell Claude!"
